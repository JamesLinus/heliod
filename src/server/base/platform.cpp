#include "base/platform.h"

static int _platform_bitiness = sizeof(void *) * 8;
static const char *_platform_subdir = PRODUCT_PLATFORM_SUBDIR;

void platform_set(const char *subdir, int bits)
{
    PR_ASSERT(bits == 32 || bits == 64);

    _platform_subdir = subdir ? PERM_STRDUP(subdir) : "";
    _platform_bitiness = bits;
}

const char *platform_get_subdir(void)
{
    if (*_platform_subdir)
        return _platform_subdir;

    return NULL;
}

int platform_get_bitiness(void)
{
    return _platform_bitiness;
}
